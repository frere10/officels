package rw.akimana.officels.Models;

public class Mark {
    private String course, chapiter, exam, userMarks, marksWeight;

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public String getChapiter() {
        return chapiter;
    }

    public void setChapiter(String chapiter) {
        this.chapiter = chapiter;
    }

    public String getExam() {
        return exam;
    }

    public void setExam(String exam) {
        this.exam = exam;
    }

    public String getUserMarks() {
        return userMarks;
    }

    public void setUserMarks(String userMarks) {
        this.userMarks = userMarks;
    }

    public String getMarksWeight() {
        return marksWeight;
    }

    public void setMarksWeight(String marksWeight) {
        this.marksWeight = marksWeight;
    }
}
